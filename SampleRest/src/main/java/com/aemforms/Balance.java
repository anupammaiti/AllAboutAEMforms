package com.aemforms;

public class Balance {
	public String year;
	public double balance;
	public void setYear(String year)
	{
		this.year = year;
	}
	public String getYear()
	{
		return this.year;
	}
	public void setBalance(double balance)
	{
		this.balance = balance;
		
	}
	public double getBalance()
	{
		return this.balance;
	}
	

}
