package com.aemforms;

public class ActivitySummary {
	public double deposits;
	public double withdrawals;
	public String beginBalanceDate;
	public String endBalanceDate;
	public String beginBalance;
	public String endBalance;
	public void setDeposits(double deposits)
	{
		this.deposits = deposits;
	}
	public double getDeposits()
	{
		return this.deposits;
	}
	public void setWithdrawals(double withdrawals)
	{
		this.withdrawals = withdrawals;
	}
	public double getWithdrawals()
	{
		return this.withdrawals;
	}
	public void setBeginBalanceDate(String beginBalanceDate)
	{
		this.beginBalanceDate = beginBalanceDate;
	}
	public String getBeginBalanceDate()
	{
		return this.beginBalanceDate;
	}
	
	public void setEndBalanceDate(String endBalanceDate)
	{
		this.endBalanceDate = endBalanceDate;
	}
	public String getEndBalanceDate()
	{
		return this.endBalanceDate;
	}
	public void setBeginBalance(String beginBalance)
	{
		this.beginBalance = beginBalance;
	}
	public String getBeginBalance()
	{
		return this.beginBalance;
	}
	public void setEndBalance(String endBalance)
	{
		this.endBalance = endBalance;
	}
	public String getEndBalance()
	{
		return this.endBalance;
	}
	

}
